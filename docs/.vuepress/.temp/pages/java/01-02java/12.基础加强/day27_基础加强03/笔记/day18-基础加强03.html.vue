<h2 id="_2-日志" tabindex="-1"><a class="header-anchor" href="#_2-日志" aria-hidden="true">#</a> 2.日志</h2>
<h3 id="_2-1概述【理解】" tabindex="-1"><a class="header-anchor" href="#_2-1概述【理解】" aria-hidden="true">#</a> 2.1概述【理解】</h3>
<ul>
<li>
<p>概述</p>
<p>程序中的日志可以用来记录程序在运行的时候点点滴滴。并可以进行永久存储。</p>
</li>
<li>
<p>日志与输出语句的区别</p>
<table>
<thead>
<tr>
<th></th>
<th>输出语句</th>
<th>日志技术</th>
</tr>
</thead>
<tbody>
<tr>
<td>取消日志</td>
<td>需要修改代码，灵活性比较差</td>
<td>不需要修改代码，灵活性比较好</td>
</tr>
<tr>
<td>输出位置</td>
<td>只能是控制台</td>
<td>可以将日志信息写入到文件或者数据库中</td>
</tr>
<tr>
<td>多线程</td>
<td>和业务代码处于一个线程中</td>
<td>多线程方式记录日志，不影响业务代码的性能</td>
</tr>
</tbody>
</table>
</li>
</ul>
<h3 id="_2-2日志体系结构和log4j【理解】" tabindex="-1"><a class="header-anchor" href="#_2-2日志体系结构和log4j【理解】" aria-hidden="true">#</a> 2.2日志体系结构和Log4J【理解】</h3>
