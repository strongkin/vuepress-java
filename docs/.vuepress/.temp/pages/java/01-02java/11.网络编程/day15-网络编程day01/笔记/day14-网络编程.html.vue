<h3 id="_1-3-ip地址【理解】" tabindex="-1"><a class="header-anchor" href="#_1-3-ip地址【理解】" aria-hidden="true">#</a> 1.3 IP地址【理解】</h3>
<p>IP地址：是网络中设备的唯一标识</p>
<ul>
<li>
<p>IP地址分为两大类</p>
<ul>
<li>
<p>IPv4：是给每个连接在网络上的主机分配一个32bit地址。按照TCP/IP规定，IP地址用二进制来表示，每个IP地址长32bit，也就是4个字节。例如一个采用二进制形式的IP地址是“11000000 10101000 00000001 01000010”，这么长的地址，处理起来也太费劲了。为了方便使用，IP地址经常被写成十进制的形式，中间使用符号“.”分隔不同的字节。于是，上面的IP地址可以表示为“192.168.1.66”。IP地址的这种表示法叫做“点分十进制表示法”，这显然比1和0容易记忆得多</p>
</li>
<li>
<p>IPv6：由于互联网的蓬勃发展，IP地址的需求量愈来愈大，但是网络地址资源有限，使得IP的分配越发紧张。为了扩大地址空间，通过IPv6重新定义地址空间，采用128位地址长度，每16个字节一组，分成8组十六进制数，这样就解决了网络地址资源数量不够的问题</p>
</li>
</ul>
